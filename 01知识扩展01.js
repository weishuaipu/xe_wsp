// 1.SPA
// 表示单页Web应用
// single page web application
// 指的是只有一张Web页面的应用
// 单页面的应用，只加载当个HTML页面，并在用户与页面交互时，动态刷新页面
// 为什么要用SPA？
// 1.速度更快
// 2.逻辑与数据相分离采用经典的MVC开发模式，前后端分离
// 3.各类数据交互均通过 Ajax来实现
// 4.再URL 均采用#来作为当前视图的地址 通过修改#好后面的参数方式页面重载

// 2.基准字体大小设置
// 答：320px像素的移动端视图中，最小字体应当不小于12号字体
// 依照设备的宽度来进行等比例换算，720像素 就是 12/320 * 720 =27
//因为字号应当为偶数，则27 = 应当设置为28；
// 并不是说像素增大 字号就增大 要参考基准字号 不小于最小字号就行

